#include <stdio.h>
#include <Windows.h>

// Функция для нахождения наименьшего положительного числа с использованием цикла while
void naim_pol_while(int k, int n, int min) {
	printf("Введите число n: ");
	scanf_s("%d", &n);
	min = n; // Первое число считается минимальным
	int i = 1;
	while (i < k) {
		printf("Введите число n: ");
		scanf_s("%d", &n);
		// Проверка, является ли число положительным
		if (n > 0) {
			// Если min уже содержит положительное число
			if (min > 0) {
				if (n < min)
					min = n;
			}
			else
				min = n; // Первое положительное число становится минимальным
		}
		i++;
	}
	(min > 0) ? printf("Наименьшее положительное число: %d", min) : printf("Положительных чисел нет.");
}

// Функция для нахождения наименьшего положительного числа с использованием цикла do-while
void naim_pol_do_while(int k, int n, int min) {
	printf("Введите число n: ");
	scanf_s("%d", &n);
	min = n; // Первое число считается минимальным
	// Проверка, что длина последовательности больше 1
	if (k > 1) {
		int i = 1;
		// Цикл do-while выполняется хотя бы один раз
		do {
			printf("Введите число n: ");
			scanf_s("%d", &n);
			i++;
			if (n > 0) {
				if (min > 0) {
					if (n < min)
						min = n;
				}
				else
					min = n;
			}
		} while (i < k);
	}
	(min > 0) ? printf("Наименьшее положительное число: %d", min) : printf("Положительных чисел нет.");
}

// Функция для нахождения наименьшего положительного числа с использованием цикла for
void naim_pol_for(int k, int n, int min) {
	printf("Введите число n: ");
	scanf_s("%d", &n);
	min = n; // Первое число считается минимальным
	for (int i = 1; i < k;) {
		printf("Введите число n: ");
		scanf_s("%d", &n);
		if (n > 0) {
			if (min > 0) {
				if (n < min)
					min = n;
			}
			else
				min = n;
		}
		i++;
	}
	(min > 0) ? printf("Наименьшее положительное число: %d", min) : printf("Положительных чисел нет.");
}

int main() {
	SetConsoleCP(1251);
	SetConsoleOutputCP(1251);
	int k, n = 0, min = 0; // Переменные для длины последовательности, текущего числа и минимального числа
	int zikl; // Переменная для выбора типа цикла
	printf("Вывод наименьшего положительного значения из вводимой последовательности.\nКакой цикл хотите использовать?\n1) while\n2) do-while\n3) for\nВаш ответ: ");
	scanf_s("%d", &zikl);

	// Обработка выбора пользователя
	if (zikl == 1) {
		printf("\nС циклом while:\n\n");
		printf("Введите целое число k (длина последовательности): ");
		scanf_s("%d", &k);
		// Проверка корректности длины последовательности
		(k > 0) ? naim_pol_while(k, n, min) : printf("Последовательность должна быть положительной.");
	}
	else if (zikl == 2) {
		printf("\nС циклом do-while:\n\n");
		printf("Введите целое число k (длина последовательности): ");
		scanf_s("%d", &k);
		(k > 0) ? naim_pol_do_while(k, n, min) : printf("Последовательность должна быть положительной.");
	}
	else if (zikl == 3) {
		printf("\nС циклом for:\n\n");
		printf("Введите целое число k (длина последовательности): ");
		scanf_s("%d", &k);
		(k > 0) ? naim_pol_for(k, n, min) : printf("Последовательность должна быть положительной.");
	}
	else
		printf("Такого варианта ответа нет.");

	return 0;
}
