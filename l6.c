#include <stdio.h>
#include <ctype.h>
#include <string.h>
#define N 1000

int main() {
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);
    char mas[N];
    printf("Введите строку на английском: ");
    fgets(mas, N, stdin); // Безопасное чтение строки от пользователя
    int i = 0; // Инициализация счетчика

    // Главный внешний цикл - проход по всей строке
    while (i < strlen(mas)) {
        int start = -1, end = -1; // Переменные для позиций начала и конца слова

        // Поиск начала слова (первой буквы)
        while (i < strlen(mas)) {
            if (isalpha(mas[i]) != 0) { // Если текущий символ - буква
                start = i; // Запоминаем позицию начала слова
                break; // Выходим из цикла поиска начала
            }
            i++; // Переходим к следующему символу
        }

        // Поиск конца слова (последней буквы)
        while (i < strlen(mas) - 1) {
            // Если текущий символ - буква, а следующий - не буква
            if (isalpha(mas[i]) != 0 && isalpha(mas[i + 1]) == 0) {
                end = i; // Запоминаем позицию конца слова
                break; // Выходим из цикла поиска конца
            }
            i++; // Переходим к следующему символу
        }

        // Проверка: найдены ли начало и конец слова
        if (0 <= start && 0 <= end) {
            // Проверка: совпадает ли первая и последняя буква слова
            if (mas[start] == mas[end]) {
                // Вывод слова от начала до конца
                while (start <= end) {
                    printf("%c", mas[start]); // Посимвольный вывод слова
                    start++;
                }
                printf(" ");
            }
        }
        i++; // Переход к следующей позиции для поиска нового слова
    }
    return 0;
}
